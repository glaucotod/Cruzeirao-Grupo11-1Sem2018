package sistema.modelos;

public class Time {
	int id;
	String nome;
	String responsavel;
	
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getResponsavel() {
		return responsavel;
	}
	public void setResponsavel(String responsavel) {
		this.responsavel = responsavel;
	}
	public int getId() {
		return id;
	}
	
}
